package com.jtp.hcontactlist.classlist;

public class Students {
    private int id;
    private String sname;
    private String smob;
    private String semail;
    private String sgender;
    private String scourse1;
    private String scourse2;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSname() {
        return sname;
    }

    public void setSname(String sname) {
        this.sname = sname;
    }

    public String getSmob() {
        return smob;
    }

    public void setSmob(String smail) {
        this.smob = smail;
    }

    public String getSemail() {
        return semail;
    }

    public void setSemail(String semail) {
        this.semail = semail;
    }

    public String getSgender() {
        return sgender;
    }

    public void setSgender(String sgender) {
        this.sgender = sgender;
    }

    public String getScourse1() {
        return scourse1;
    }

    public void setScourse1(String scourse1) {
        this.scourse1 = scourse1;
    }

    public String getScourse2() {
        return scourse2;
    }

    public void setScourse2(String scourse2) {
        this.scourse2 = scourse2;
    }
}
